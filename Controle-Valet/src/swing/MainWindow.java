    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        Valet v = new Valet(
                txt_modelo.getText(),
                txt_placa.getText(),
                new Date()
        );
        modeloValet.adicionar(v);
        this.limparCampos();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        this.limparCampos();
    }//GEN-LAST:event_btnNovoActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        Valet v = modeloValet.getValet(tabela.getSelectedRow());
        v.setSaida(new Date());
        txt_modelo.setText(v.getModelo());
        txt_placa.setText(v.getPlaca());
        lbl_entrada.setText(df.format(v.getEntrada()));
        v.setPreco(this.calcularPreco(v.getEntrada(), v.getSaida()));
        lbl_preco.setText("R$ " + String.valueOf(v.getPreco()));
        selecionado = v;
    }//GEN-LAST:event_tabelaMouseClicked

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        modeloValet.remover(selecionado);
        limparCampos();
    }//GEN-LAST:event_btnFinalizarActionPerformed
   
     private Double calcularPreco(Date entrada, Date saida) {   
         long diferenca = saida.getTime() - entrada.getTime();
         int horas = (int) (diferenca / 1000 / 60 / 60);
         int minutos = (int) ((diferenca / 1000 / 60) - (horas * 60));
         
         double preco = horas * 4;
         if (minutos != 0) preco += 4;
         
         return preco;
     }
     
    private void limparCampos() {
        txt_modelo.setText("");
        txt_placa.setText("");
        lbl_entrada.setText("");
        lbl_preco.setText("");
    }
